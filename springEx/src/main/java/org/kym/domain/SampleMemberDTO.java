package org.kym.domain;

public class SampleMemberDTO {
	private String id;  // 단 변수명이 html의 name명과 동일해야 한다. 
	private String pw;
	private String name;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
